package com.spring.admintool.model;

public class CompanyProjectModel {

	private String c_name;
	private String c_tax_no;
	private String c_type;
	private String c_statu;
	private String p_count;
	
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_tax_no() {
		return c_tax_no;
	}
	public void setC_tax_no(String c_tax_no) {
		this.c_tax_no = c_tax_no;
	}
	public String getC_type() {
		return c_type;
	}
	public void setC_type(String c_type) {
		this.c_type = c_type;
	}
	public String getC_statu() {
		return c_statu;
	}
	public void setC_statu(String c_statu) {
		this.c_statu = c_statu;
	}
	public String getP_count() {
		return p_count;
	}
	public void setP_count(String p_count) {
		this.p_count = p_count;
	}
	
}
